# What do you understand by programming language?

# A programming language is a set of instructions written by a programmer to deliver instructions to the computer to perform 

# and accomplish a task.

# ===============================================================


# High Level language

# A high-level language (HLL) is a programming language such as C or Python, that allows a programmer to develop programmes 

# that are independent of the type of machine they are running on.

# Ex - C, C++,Python

# ===============================================================


# Low Level language

# Low-level languages are programming languages that provide little or no abstraction from a computer's instruction set 

# architecture. They are closer to machine code and are often used for system programming, hardware manipulation, and 

# performance-critical tasks.

# Ex - Machine Language, Assembly Language.

# ===============================================================



# Interpreted Programming language

# An Interpreted language executes its statements line by line. 

# Languages such as Python, JavaScript, R, PHP, and Ruby are prime examples of Interpreted languages.

# Programs written in an interpreted language runs directly from the source code, with no intermediary compilation step.

# Interpreted language follows one step to get from source code to execution.

# ===============================================================



# Complied Programming language

#  A compiled language is a programming language that is converted into machine code so that the processor can execute it.

#  Programs that are compiled into native machine code tend to be faster than interpreted code.

# ===============================================================


# Compilation and Runtime 

# Compilation is the process of translating source code written in a high-level programming language into a lower-level 

# language, such as machine code, that can be executed by a computer.

# Runtime is the final phase of the program lifecycle in which the machine executes the program's code.

# ===============================================================


# What is Python? 

# Python is a high-level, interpreted, general-purpose programming language.

